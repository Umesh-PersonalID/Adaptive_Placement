df = pd.DataFrame(data, columns=["trial","m","uncovered","overlap"])

# -----------------------------
# Compute Statistics
# -----------------------------
stats = df.groupby("m").agg(
    mean_uncovered=("uncovered","mean"),
    var_uncovered=("uncovered", lambda x: np.var(x, ddof=1)),
    mean_overlap=("overlap","mean"),
    var_overlap=("overlap", lambda x: np.var(x, ddof=1)),
).reset_index()

stats["mean_per_covered"] = (
    (region_area - stats["mean_uncovered"]) / region_area
) * 100

stats["std_uncovered"] = np.sqrt(stats["var_uncovered"])
stats["std_overlap"] = np.sqrt(stats["var_overlap"])
stats["std_per_covered"] = stats["std_uncovered"] / region_area * 100

# -----------------------------
# Plot
# -----------------------------
fig, ax1 = plt.subplots(figsize=(10,6))

ax1.errorbar(
    stats["m"],
    stats["mean_per_covered"],
    yerr=stats["std_per_covered"],
    marker='o',
    color='red',
    capsize=4,
    label="Mean Coverage %"
)

ax1.set_xlabel("m", fontsize=13)
ax1.set_ylabel("Coverage %", fontsize=13)
ax1.set_ylim(89, 100)

ax2 = ax1.twinx()

ax2.errorbar(
    stats["m"],
    stats["mean_overlap"],
    yerr=stats["std_overlap"],
    marker='s',
    linestyle='--',
    capsize=4,
    label="Mean Overlap %"
)

ax2.set_ylabel("Overlap %", fontsize=13)
ax2.set_ylim(0, 20)

plt.title(f"Region 4: Mean Coverage % and Overlap % vs m", fontsize=15)

lines1, labels1 = ax1.get_legend_handles_labels()
